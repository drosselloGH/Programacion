//programa que repite 10 veces una frase
public class Ejemplo07 {

	public static void main(String[] args) {

		int count = 1; //creamos un contador
		
		while (count <= 10) {
			System.out.println(count+ ") Hola, me llamo Dani");
			count++;

		}

		System.out.println("Ya me he cansado de escribir");
		
	}

}
