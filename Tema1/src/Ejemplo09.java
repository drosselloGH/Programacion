public class Ejemplo09 {

	public static void main(String[] args) {

		boolean a = true;
		boolean b = true;
		boolean c = false;
		boolean d = false;
		
		//operaciones AND
		
		System.out.println(a && b);
		System.out.println(a && c);
		
		//operaciones OR
		
		System.out.println(a || b);
		System.out.println(c || d);
		
		//operacion NOT
		
		System.out.println(!a);
		System.out.println(!c);




	}

}
