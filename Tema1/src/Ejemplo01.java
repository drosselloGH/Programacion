
public class Ejemplo01 {

	public static void main(String[] args) {
		
		//Variable de tipo String (Cadena de caracteres)
		
		String apellidos; //declarada variable "apellidos"
		
		apellidos = "Pérez López"; //inicializamos la variable
		
		System.out.println(apellidos); //mostramos la variable "apellidos"
		
		apellidos = "Martínez Almeida"; //reasignamos la variable "apellidos"
		
		System.out.println(apellidos); //mostramos la variable reasignada

	}

}
